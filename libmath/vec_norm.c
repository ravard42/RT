#include "libmath.h"

t_vect		vec_norm(t_vect v)
{
	t_vect	n;
	float	len;

	len = vec_len(v);
	n.x = v.x / len;
	n.y = v.y / len;
	n.z = v.z / len;
	return (n);
}
