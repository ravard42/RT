#include "libmath.h"

t_vect		cross(t_vect a, t_vect b)
{
	t_vect	c;

	c.x = a.y * b.z - b.y * a.z;
	c.y = a.z * b.x - b.z * a.x;
	c.z = a.x * b.y - b.x * a.y;
	return (c);
}
