#include "rt.h"

void	vect_init(t_vect *v)
{
	v->x = 0;
	v->y = 0;
	v->x = 0;
}

void	base_init(t_base *b)
{
	vect_init(&(b->vx));
	vect_init(&(b->vy));
	vect_init(&(b->vz));
}
