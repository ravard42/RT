#include "rt.h"

t_vect	sinus_plane(t_ray *r)
{
	t_vect	n;
	float	z;

	n = r->o_inter->axe;
	z = r->p[1].z;
	n.z = sin(z * 4);
	n = vec_norm(n);
	return (n);
}
